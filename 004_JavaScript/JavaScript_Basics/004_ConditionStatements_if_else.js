//In JavaScript, the if...else statement is used to control the flow of your code based on a condition. It allows you to execute different blocks of code 
//depending on whether the condition is true or false. 



// If Else 
// #1 - Single If Else
let number = 10;
if(number > 10)
{
    console.log("Number is greater than 10");
}
else 
{
    console.log("Number is lesser or equal to 10");
}

// #2 - Multiple if else 
if(number < 10)
{
    console.log("Number is lesser than 10");
}
else if(number > 10)
{
    console.log("Number is greater than 10");   
}
else{
    console.log("Number is equal to 10");
}
