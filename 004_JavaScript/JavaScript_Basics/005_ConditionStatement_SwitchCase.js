//In JavaScript, the switch statement is used to perform different actions based on different conditions. It's a control structure that provides a more efficient
//alternative to using multiple if...else if...else statements when you have a single value to test against multiple possible cases.

//example

let weekNumber = 4;
switch (weekNumber) {
    case 1:
        console.log("Monday");
        break;
    
    case 2:
        console.log("Tuesday");
        break;
    
    case 3:
        console.log("Wednesday");
        break;
    
    case 4:
        console.log("Thursday");
        break;
    
    case 5:
        console.log("Friday");
        break;
    
    case 6:
        console.log("Saturday");
        break;    
    
    case 7:
        console.log("Sunday");
        break;
    
    default:
        console.log("Invalid")
}
